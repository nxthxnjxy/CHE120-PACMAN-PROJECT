def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    """Return offset of point in tiles."""
    x = floor((point.x + 200) / 20)  # Adjusted x calculation
    y = floor((180 - point.y) / 20)   # Adjusted y calculation
    index = int(x + y * 20)

    if 0 <= index < len(tiles):
        return index
    else:
        return -1  # Return -1 if the index is out of range

def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if index == -1:
        return False

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if index == -1:
        return False

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        path = astar((floor(point.x / 20), floor(point.y / 20)), (floor(pacman.x / 20), floor(pacman.y / 20)), tiles)

        if path:
            next_point = vector(path[0][0] * 20, path[0][1] * 20)
            direction_to_next = calculate_direction_to_target(point, next_point)
            direction_to_next.normalize()
            course.x = direction_to_next.x
            course.y = direction_to_next.y
        else:
            options = [vector(5, 0), vector(-5, 0), vector(0, 5), vector(0, -5)]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        if valid(point + course):
            point.move(course)

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 100)

def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

